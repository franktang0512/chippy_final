		function changename(cname) {
		  let index = cname.id.substring(5);
		  let inputValue = cname.value;
		  if(inputValue==""){
			  return;
		  }
		  // 驗證是否只包含中文、英文字符和數字，且長度不超過5
		  let isValid = /^[\u4e00-\u9fa5a-zA-Z0-9]{1,5}$/.test(inputValue);

		  if (isValid) {
			allclasses.classes[index].classname = inputValue;
			cname.style.border = ""; // 恢復邊框樣式
		  } else {
			// 清空值、設置紅色邊框
			cname.value = "";
			cname.style.border = "1px solid red";
			alert("請只輸入一個班級！");
		  }
		}
		 
		 
		 
		 
